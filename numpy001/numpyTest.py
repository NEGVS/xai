import numpy as np

# create arr
arr = np.array([1, 2, 3, 4, 5])

# print arr
print(arr)

# count sum arr
sum_arr = np.sum(arr)
print('sum of arr\n', sum_arr)

# create 2 X 2 matrix
matrix = np.array([[1, 2], [3, 4]])
# print
print(matrix)
# matrix transfer
transpose_matrix = np.transpose(matrix)

